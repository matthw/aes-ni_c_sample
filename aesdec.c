__m128i aes_dec(uint8_t *plaintext) {
    

    __m128i pt = _mm_loadu_si128((__m128i *)plaintext);

    __m128i enc;


    // initial message is XORed with key
    enc = _mm_xor_si128(pt,  _mm_loadu_si128((__m128i *)round_key_A));   // this is where the password is used

    // then aes rounds are applied on them, always with the same keys

    enc = _mm_aesdec_si128(enc, _mm_aesimc_si128(_mm_loadu_si128((__m128i *)round_key_9)));    // aes
    enc = _mm_aesdec_si128(enc, _mm_aesimc_si128(_mm_loadu_si128((__m128i *)round_key_8)));    // aes
    enc = _mm_aesdec_si128(enc, _mm_aesimc_si128(_mm_loadu_si128((__m128i *)round_key_7)));    // aes
    enc = _mm_aesdec_si128(enc, _mm_aesimc_si128(_mm_loadu_si128((__m128i *)round_key_6)));    // aes
    enc = _mm_aesdec_si128(enc, _mm_aesimc_si128(_mm_loadu_si128((__m128i *)round_key_5)));    // aes
    enc = _mm_aesdec_si128(enc, _mm_aesimc_si128(_mm_loadu_si128((__m128i *)round_key_4)));    // aes
    enc = _mm_aesdec_si128(enc, _mm_aesimc_si128(_mm_loadu_si128((__m128i *)round_key_3)));    // aes
    enc = _mm_aesdec_si128(enc, _mm_aesimc_si128(_mm_loadu_si128((__m128i *)round_key_2)));    // aes
    enc = _mm_aesdec_si128(enc, _mm_aesimc_si128(_mm_loadu_si128((__m128i *)round_key_1)));    // aes
    enc = _mm_aesdeclast_si128(enc, _mm_loadu_si128((__m128i *)aes_key));    // aes
    

    return enc;

}


